package pack2;

public class PositiveandNegativeNum {

	public static void main(String[] args) {


		double num=0;

		if(num < 0){
			System.out.println("num is negative");
			
		}else if(num > 0){
			System.out.println("num is positive");
		}
		else{
			System.out.println("num is zero");
		}
	}

}
